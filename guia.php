        <section>
            <h2>Introdução</h2>
            <p>Bem-vindo ao nosso software de criação de currículos online. Este guia foi criado para ajudá-lo a entender como utilizar todas as funcionalidades disponíveis para criar e imprimir seu currículo de maneira rápida e eficiente.</p>
        </section>
        
        <section>
            <h2>Passo 1: Acesso à Ferramenta</h2>
            <p>Para acessar a ferramenta de criação de currículos, basta abrir o arquivo <code>index.html</code> em seu navegador. Você verá a tela inicial onde poderá inserir suas informações pessoais e profissionais.</p>
        </section>
        
        <section>
            <h2>Passo 2: Inserção de Dados</h2>
            <p>Preencha os campos disponíveis com suas informações:</p>
            <ul>
                <li><strong>Nome:</strong> Digite seu nome completo no campo de texto.</li>
                <li><strong>Idade:</strong> Informe sua idade.</li>
                <li><strong>E-mail:</strong> Insira seu endereço de e-mail.</li>
                <li><strong>Apresentação:</strong> Escreva uma breve apresentação sobre você.</li>
                <li><strong>Formação:</strong> Informe sua formação acadêmica.</li>
                <li><strong>Qualificações:</strong> Liste suas qualificações e habilidades.</li>
                <li><strong>Experiências:</strong> Descreva suas experiências profissionais.</li>
            </ul>
        </section>
        
        <section>
            <h2>Passo 3: Adicionar Informações</h2>
            <p>Utilize os botões de ação para adicionar, limpar ou deletar informações conforme necessário:</p>
            <ul>
                <li><strong>Adicionar:</strong> Clique no botão <code>Adicionar</code> para inserir as informações no currículo.</li>
                <li><strong>Deletar:</strong> Clique no botão <code>Deletar</code> para remover informações inseridas.</li>
            </ul>
        </section>
        
        <section>
            <h2>Passo 4: Gerar Currículo</h2>
            <p>Depois de inserir todas as informações, clique no botão <code>Gerar Currículo</code> para enviar os dados e gerar a visualização do currículo.</p>
        </section>
        
        <section>
            <h2>Passo 5: Imprimir Currículo</h2>
            <p>Na página de visualização do currículo, você verá um botão <code>Imprimir Currículo</code>. Clique nele para ocultar o botão e abrir a caixa de diálogo de impressão do seu navegador. Siga as instruções para imprimir seu currículo.</p>
        </section>
        
